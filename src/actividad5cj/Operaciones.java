public class Operaciones {
    
    float x=0;
    float y=0;
    
    public float suma(float x, float y){
        float result=x+y;
        return result;
    }
    
     public float resta(float x, float y){
        float result=x-y;
        return result;
    }
     
      public float multiplicacion(float x, float y){
        float result=x*y;
        return result;
    }
      
       public float division(float x, float y){
        float result=x/y;
        return result;
    }
        public float modulo(float x, float y){
        float result=x%y;
        return result;
    }
    
}
